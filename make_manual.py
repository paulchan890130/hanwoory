doc = Document()

# ─────────────────────────────────────────────────────────────
# 헬퍼 함수
# ─────────────────────────────────────────────────────────────
def _font(run, name="맑은 고딕", size=10, bold=False, italic=False, color=None):
    run.font.name = name
    run.font.size = Pt(size)
    run.bold = bold
    run.italic = italic
    if color:
        run.font.color.rgb = RGBColor(*color)

def h1(text):
    p = doc.add_paragraph()
    p.paragraph_format.space_before = Pt(20)
    p.paragraph_format.space_after  = Pt(6)
    r = p.add_run(text); _font(r, size=15, bold=True, color=(33,84,143))

def h2(text):
    p = doc.add_paragraph()
    p.paragraph_format.space_before = Pt(12)
    p.paragraph_format.space_after  = Pt(3)
    p.paragraph_format.left_indent  = Cm(0.4)
    r = p.add_run(text); _font(r, size=12, bold=True, color=(40,110,40))

def h3(text, color=(60,60,100)):
    p = doc.add_paragraph()
    p.paragraph_format.space_before = Pt(8)
    p.paragraph_format.space_after  = Pt(2)
    p.paragraph_format.left_indent  = Cm(0.7)
    r = p.add_run(text); _font(r, size=11, bold=True, color=color)

def body(text, indent=1.0):
    p = doc.add_paragraph()
    p.paragraph_format.space_after  = Pt(3)
    p.paragraph_format.left_indent  = Cm(indent)
    r = p.add_run(text); _font(r, size=10)

def bullet(text, indent=1.2, sym="•"):
    p = doc.add_paragraph()
    p.paragraph_format.space_after  = Pt(2)
    p.paragraph_format.left_indent  = Cm(indent)
    r = p.add_run(f"{sym}  {text}"); _font(r, size=10)

def step(num, text, indent=1.5):
    p = doc.add_paragraph()
    p.paragraph_format.space_after  = Pt(2)
    p.paragraph_format.left_indent  = Cm(indent)
    r = p.add_run(f"[{num}]  {text}"); _font(r, size=10)

def tip(text):
    p = doc.add_paragraph()
    p.paragraph_format.space_before = Pt(4)
    p.paragraph_format.space_after  = Pt(4)
    p.paragraph_format.left_indent  = Cm(1.0)
    r = p.add_run(f"  TIP  |  {text}"); _font(r, size=9, italic=True, color=(100,70,0))

def warn(text):
    p = doc.add_paragraph()
    p.paragraph_format.space_before = Pt(3)
    p.paragraph_format.space_after  = Pt(3)
    p.paragraph_format.left_indent  = Cm(1.0)
    r = p.add_run(f"  주의  |  {text}"); _font(r, size=9, italic=True, color=(170,50,0))

def divider():
    p = doc.add_paragraph()
    p.paragraph_format.space_before = Pt(8)
    p.paragraph_format.space_after  = Pt(8)
    r = p.add_run("─" * 65); _font(r, size=7, color=(200,200,200))

