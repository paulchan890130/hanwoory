DB_PATH = ROOT / "backend" / "data" / "immigration_guidelines_db_v2.json"

with open(DB_PATH, encoding="utf-8") as f:
    db = json.load(f)

rows = db.get("master_rows", [])
idx = {r["row_id"]: i for i, r in enumerate(rows)}

def patch(row_id: str, updates: dict):
    if row_id not in idx:
        print(f"[WARN] row_id {row_id} not found — skipped")
        return
    rows[idx[row_id]].update(updates)
    print(f"[PATCH] {row_id} updated")

